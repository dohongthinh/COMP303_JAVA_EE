package sales;

public class Product {
	String name;
	String price;
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	public String getName()
	{
		return name;
	}
	
	public void setPrice(String price)
	{
		this.price = price;
	}
	
	public String getPrice()
	{
		return price;
	}
	
	
}
